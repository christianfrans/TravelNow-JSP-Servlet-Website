	// <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
	/**
	 * Handles the HTTP <code>GET</code> method.
	 *
	 * @param request servlet request
	 * @param response servlet response
	 * @throws ServletException if a servlet-specific error occurs
	 * @throws IOException if an I/O error occurs
	 */
	@Override
	protected void doGet(HttpServletRequest request, HttpServletResponse response)
			throws ServletException, IOException {
		try {
			String id = request.getParameter("id");
            
            AdminController ac = new AdminController();
            AdminModel model = ac.showPackets(id);
            
            request.setAttribute("item", model);
            
            RequestDispatcher dispatch = request.getRequestDispatcher("/admin-packets-edit.jsp");
            dispatch.forward(request, response);
		} catch (SQLException ex) {
			Logger.getLogger(AdminPacketsEdit.class.getName()).log(Level.SEVERE, null, ex);
		}
	}

	/**
	 * Handles the HTTP <code>POST</code> method.
	 *
	 * @param request servlet request
	 * @param response servlet response
	 * @throws ServletException if a servlet-specific error occurs
	 * @throws IOException if an I/O error occurs
	 */
	@Override
	protected void doPost(HttpServletRequest request, HttpServletResponse response)
			throws ServletException, IOException {
		try {
			String id = request.getParameter("id");
			String price = request.getParameter("price");
			String description = request.getParameter("description");
			String name = request.getParameter("name");
			
			AdminModel model = new AdminModel();
			model.setId(id);
			model.setPrice(Integer.parseInt(price));
			model.setDescription(description);
			model.setName(name);
			
			AdminController controller = new AdminController();
			boolean update = controller.updatePackets(model);
			
			if(update){
				response.sendRedirect("admin");
			}
		} catch (SQLException ex) {
			Logger.getLogger(AdminPacketsEdit.class.getName()).log(Level.SEVERE, null, ex);
		}
	}

